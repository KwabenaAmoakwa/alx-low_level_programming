#include <stdio.h>

/**
 * add - returns the sum of 2 nos
 * @a: argument
 * @b: argument
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the difference of 2 nos
 * @a: argument
 * @b: argument
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of 2 nos
 * @a: argument
 * @b: argument
 *
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the quotient of 2 nos
 * @a: argument
 * @b: argument
 *
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the modulo of 2 nos
 * @a: argument
 * @b: argument
 *
 * Return: quotient
 */
int mod(int a, int b)
{
	return (a % b);
}
